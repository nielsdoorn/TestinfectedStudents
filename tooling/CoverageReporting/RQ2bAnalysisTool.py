with open(output_folder+"combined_tests.csv", 'wb') as myfile:
    wr = csv.writer(myfile, quoting=csv.QUOTE_ALL)

    headers = [ "ruleset", "rule", "count with tests", "count without tests" ]
    wr.writerow(headers)

    # combine them
    for ruleset in without_test_violations:
        if ruleset not in combined_violations:
            combined_violations[ruleset] = {}
        for rule in without_test_violations[ruleset]:
            if rule not in combined_violations[ruleset]:
                combined_violations[ruleset][rule] = {}
                combined_violations[ruleset][rule]["without"] = 0
                combined_violations[ruleset][rule]["with"] = 0
            old_value = combined_violations[ruleset][rule]["without"]
            new_value = old_value + without_test_violations[ruleset][rule]
            combined_violations[ruleset][rule]["without"] = new_value
    for ruleset in with_test_violations:
        if ruleset not in combined_violations:
            combined_violations[ruleset] = {}
        for rule in with_test_violations[ruleset]:
            if rule not in combined_violations[ruleset]:
                combined_violations[ruleset][rule] = {}
                combined_violations[ruleset][rule]["without"] = 0
                combined_violations[ruleset][rule]["with"] = 0
            old_value = combined_violations[ruleset][rule]["with"]
            new_value = old_value + with_test_violations[ruleset][rule]
            combined_violations[ruleset][rule]["with"] = new_value
    
    # write them
    for ruleset in combined_violations:
        for rule in combined_violations[ruleset]:
            count_without =    combined_violations[ruleset][rule]["without"]
            count_with =       combined_violations[ruleset][rule]["with"]
            line = [ruleset, rule, count_with, count_without]
            wr.writerow(line)


